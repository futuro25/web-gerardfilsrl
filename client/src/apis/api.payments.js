const BASE_URL = "/api/payments";

export const usePaymentsQuery = async () => {
  const res = await fetch(`${BASE_URL}`, {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  });
  if (!res.ok) {
    throw new Error("Error en la petici贸n");
  }
  return res.json();
};

